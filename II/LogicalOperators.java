package II;

public class LogicalOperators {
    public static void main(String[] args) {
        boolean x = true, y = false;

        System.out.println("x AND y: " + (x && y)); // Logical AND
        System.out.println("x OR y: " + (x || y));  // Logical OR
        System.out.println("NOT x: " + (!x));       // Logical NOT
    }
}

