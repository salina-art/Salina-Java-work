package III;

public class ArrayExample {
    public static void main(String[] args) {
        int[] numbers = {10, 20, 30, 40, 50}; // Array declaration and initialization

        System.out.println("Array elements:");
        for (int i = 0; i < numbers.length; i++) { // Iterating over the array
            System.out.println(numbers[i]);
        }
    }
}
